package com.oas.webservice.vo;

/**
 * @author Systems
 *
 */
public class SurveyCountVo {
	
	private String totalSurvey;
	private String completedSurvey;
	private String remainingSurvey;
	
	
	/**
	 * @return the totalSurvey
	 */
	public String getTotalSurvey() {
		return totalSurvey;
	}
	/**
	 * @param totalSurvey the totalSurvey to set
	 */
	public void setTotalSurvey(String totalSurvey) {
		this.totalSurvey = totalSurvey;
	}
	/**
	 * @return the completedSurvey
	 */
	public String getCompletedSurvey() {
		return completedSurvey;
	}
	/**
	 * @param completedSurvey the completedSurvey to set
	 */
	public void setCompletedSurvey(String completedSurvey) {
		this.completedSurvey = completedSurvey;
	}
	/**
	 * @return the remainingSurvey
	 */
	public String getRemainingSurvey() {
		return remainingSurvey;
	}
	/**
	 * @param remainingSurvey the remainingSurvey to set
	 */
	public void setRemainingSurvey(String remainingSurvey) {
		this.remainingSurvey = remainingSurvey;
	}
	

}

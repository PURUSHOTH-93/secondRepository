package com.oas.webservice.vo;

/**
 * @author Systems
 *
 */
public class OpenTicketsVo {
	
	private String id;
	private String fpscode;
	private String ticketno;
	private String createddate;
	private String problemreported;
	private String priority;
	private String status;
	
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the fpscode
	 */
	public String getFpscode() {
		return fpscode;
	}
	/**
	 * @param fpscode the fpscode to set
	 */
	public void setFpscode(String fpscode) {
		this.fpscode = fpscode;
	}
	/**
	 * @return the ticketno
	 */
	public String getTicketno() {
		return ticketno;
	}
	/**
	 * @param ticketno the ticketno to set
	 */
	public void setTicketno(String ticketno) {
		this.ticketno = ticketno;
	}
	/**
	 * @return the createddate
	 */
	public String getCreateddate() {
		return createddate;
	}
	/**
	 * @param createddate the createddate to set
	 */
	public void setCreateddate(String createddate) {
		this.createddate = createddate;
	}
	/**
	 * @return the problemreported
	 */
	public String getProblemreported() {
		return problemreported;
	}
	/**
	 * @param problemreported the problemreported to set
	 */
	public void setProblemreported(String problemreported) {
		this.problemreported = problemreported;
	}
	/**
	 * @return the priority
	 */
	public String getPriority() {
		return priority;
	}
	/**
	 * @param priority the priority to set
	 */
	public void setPriority(String priority) {
		this.priority = priority;
	}
	/**
	 * @return the status
	 */
	public String getStatus() {
		return status;
	}
	/**
	 * @param status the status to set
	 */
	public void setStatus(String status) {
		this.status = status;
	}
	
	

}
